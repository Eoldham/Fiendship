"""
Function used to convert a number to a string

"""


def number_to_string(num: int):
    letter = ""
    if num == 1:
        letter = "a"
    if num == 2:
        letter = "b"
    if num == 3:
        letter = "c"
    if num == 4:
        letter = "d"
    if num == 5:
        letter = "e"
    if num == 6:
        letter = "f"
    if num == 7:
        letter = "g"
    if num == 8:
        letter = "h"
    if num == 9:
        letter = "i"
    if num == 10:
        letter = "j"
    if num == 11:
        letter = "k"
    if num == 12:
        letter = "l"
    if num == 13:
        letter = "m"
    if num == 14:
        letter = "n"
    if num == 15:
        letter = "o"
    if num == 16:
        letter = "p"
    if num == 17:
        letter = "q"
    if num == 18:
        letter = "r"
    if num == 19:
        letter = "s"
    if num == 20:
        letter = "t"
    if num == 21:
        letter = "u"
    if num == 22:
        letter = "v"
    if num == 23:
        letter = "w"
    if num == 24:
        letter = "x"
    if num == 25:
        letter = "y"
    if num == 26:
        letter = "z"
    return letter
